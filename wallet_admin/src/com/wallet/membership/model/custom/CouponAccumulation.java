package com.wallet.membership.model.custom;

public class CouponAccumulation {
	
	private String cpnId;
	private String term;
	private String cpnType;
	private String cpnName;
	private String cpnAmountRate;
	private String accumCnt;
	private String amount;
	private String cpnDcAmount;
	
	public String getCpnId() {
		return cpnId;
	}
	public void setCpnId(String cpnId) {
		this.cpnId = cpnId;
	}
	public String getTerm() {
		return term;
	}
	public void setTerm(String term) {
		this.term = term;
	}
	public String getCpnType() {
		return cpnType;
	}
	public void setCpnType(String cpnType) {
		this.cpnType = cpnType;
	}
	public String getCpnName() {
		return cpnName;
	}
	public void setCpnName(String cpnName) {
		this.cpnName = cpnName;
	}
	public String getCpnAmountRate() {
		return cpnAmountRate;
	}
	public void setCpnAmountRate(String cpnAmountRate) {
		this.cpnAmountRate = cpnAmountRate;
	}
	public String getAccumCnt() {
		return accumCnt;
	}
	public void setAccumCnt(String accumCnt) {
		this.accumCnt = accumCnt;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getCpnDcAmount() {
		return cpnDcAmount;
	}
	public void setCpnDcAmount(String cpnDcAmount) {
		this.cpnDcAmount = cpnDcAmount;
	}

}
