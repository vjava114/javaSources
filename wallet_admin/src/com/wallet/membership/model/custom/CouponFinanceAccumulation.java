package com.wallet.membership.model.custom;

public class CouponFinanceAccumulation {
	private String idx;
	private String compName;
	private String cpnName;
	private String cpnDcAmount;
	private String cnt;
	private String sumPayAmount;
	private String sumDcPrice;
	private String compShareRatio;
	private String shareSumDcPrice;
	private String searchDate;
	
	
	
	
	
	public String getIdx() {
		return idx;
	}
	public void setIdx(String idx) {
		this.idx = idx;
	}
	public String getCompName() {
		return compName;
	}
	public void setCompName(String compName) {
		this.compName = compName;
	}
	public String getCpnName() {
		return cpnName;
	}
	public void setCpnName(String cpnName) {
		this.cpnName = cpnName;
	}
	public String getCpnDcAmount() {
		return cpnDcAmount;
	}
	public void setCpnDcAmount(String cpnDcAmount) {
		this.cpnDcAmount = cpnDcAmount;
	}
	public String getCnt() {
		return cnt;
	}
	public void setCnt(String cnt) {
		this.cnt = cnt;
	}
	public String getSumPayAmount() {
		return sumPayAmount;
	}
	public void setSumPayAmount(String sumPayAmount) {
		this.sumPayAmount = sumPayAmount;
	}
	public String getSumDcPrice() {
		return sumDcPrice;
	}
	public void setSumDcPrice(String sumDcPrice) {
		this.sumDcPrice = sumDcPrice;
	}
	public String getCompShareRatio() {
		return compShareRatio;
	}
	public void setCompShareRatio(String compShareRatio) {
		this.compShareRatio = compShareRatio;
	}
	public String getShareSumDcPrice() {
		return shareSumDcPrice;
	}
	public void setShareSumDcPrice(String shareSumDcPrice) {
		this.shareSumDcPrice = shareSumDcPrice;
	}
	public String getSearchDate() {
		return searchDate;
	}
	public void setSearchDate(String searchDate) {
		this.searchDate = searchDate;
	}
	
	
}
