package com.wallet.membership.model.custom;

public class CpnShareRatio {

	private String term;
	private String cpnName;
	private String shareCompType;
	private String shareCompName;
	private String shareRate;
	private String shareAmount;
	
	public String getTerm() {
		return term;
	}
	public void setTerm(String term) {
		this.term = term;
	}
	
	public String getCpnName() {
		return cpnName;
	}
	public void setCpnName(String cpnName) {
		this.cpnName = cpnName;
	}
	public String getShareCompType() {
		return shareCompType;
	}
	public void setShareCompType(String shareCompType) {
		this.shareCompType = shareCompType;
	}
	public String getShareCompName() {
		return shareCompName;
	}
	public void setShareCompName(String shareCompName) {
		this.shareCompName = shareCompName;
	}
	public String getShareRate() {
		return shareRate;
	}
	public void setShareRate(String shareRate) {
		this.shareRate = shareRate;
	}
	public String getShareAmount() {
		return shareAmount;
	}
	public void setShareAmount(String shareAmount) {
		this.shareAmount = shareAmount;
	}

}
